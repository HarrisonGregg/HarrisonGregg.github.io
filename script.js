angular.module('app', ['ngRoute'])

.controller('HomeController', ['$scope', '$route', function($scope, $route) {
    $scope.route = $route;
		$scope.projects = projects;
		$scope.posts = posts;
}])

.controller('ProjectController', function($scope, $route, $routeParams) {
	$scope.route = $route;
	$scope.project = null;

	var i;
	for(i = 0; i < projects.length; i++){
		var j;
		for(j = 0; j < projects[i].projects.length; j++){
			if(projects[i].projects[j].url==$routeParams.projectId){
				$scope.project = projects[i].projects[j];
			}
		}
	}
})

.controller('BlogController', function($scope, $route, $routeParams) {
	$scope.route = $route;
	var i;
		$scope.post = null;
	for(i = 0; i < posts.length; i++){
		if(posts[i].url==$routeParams.postId){
			$scope.post = posts[i];
		}
	}
})

.config(function($routeProvider) {
	$routeProvider
	.when('/', {
		templateUrl: 'home.html',
		controller: 'HomeController',
    activetab: ''
	})
	.when('/about', {
		templateUrl: 'about.html',
		controller: 'HomeController',
		activetab: 'about'
	})
	.when('/projects', {
		templateUrl: 'projects.html',
		controller: 'HomeController',
		activetab: 'projects'
	})
	.when('/blog', {
		templateUrl: 'blog.html',
		controller: 'HomeController',
		activetab: 'blog'
	})
	.when('/projects/:projectId', {
		templateUrl: 'project.html',
		controller: 'ProjectController',
		activetab: 'projects'
	})
	.when('/blog/:postId', {
		templateUrl: 'post.html',
		controller: 'BlogController',
		activetab: 'blog'
	})
	.otherwise({
		redirectTo:'/'
	});
});
